export async function getServerSideProps(context) {
  const res = await fetch(`${process.env.API_SERVER_URL}/api/emojis`, {
    headers: {
      'Authorization': `Bearer ${process.env.API_TOKEN}`
    }
  })
  const data = await res.json()
  return {
    props: {
      emojis: data.data
    }
  }
}

